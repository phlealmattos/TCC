   public void CadastrarUsuario() {
    UsuarioModel usuarioModel = new UsuarioModel();

    String Nome = txtnome.getText();
    String Sobrenome = txtsobrenome.getText();
    String CPF = txtcpf.getText();
    String Celular = txtcelular.getText();
    String Nascimento = txtnascimento.getText(); // Corrigir para o campo de nascimento
    String Sexo = txtsexo.getText();
    String Email = txtemail.getText();
    String Senha = txtsenha.getText();
    String Confirma_Senha = txtconfirma.getText();

    // Verifica se algum campo obrigatÃ³rio estÃ¡ vazio
    if (Nome.isEmpty() || Sobrenome.isEmpty() || CPF.isEmpty() || Celular.isEmpty() || 
        Nascimento.isEmpty() || Sexo.isEmpty() || Email.isEmpty() || Senha.isEmpty() || 
        Confirma_Senha.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos obrigatÃ³rios.", "Erro", JOptionPane.ERROR_MESSAGE);
        return; // NÃ£o prossegue com o cadastro se algum campo estiver vazio
    }

    // Restante do cÃ³digo para validar outros critÃ©rios, se necessÃ¡rio...

    // Se todos os campos estÃ£o preenchidos, prossegue com o cadastro
    usuarioModel.setNome(Nome);
    usuarioModel.setCPF(CPF);
    usuarioModel.setSobrenome(Sobrenome);
    usuarioModel.setCelular(Celular);
    usuarioModel.setNascimento(Nascimento);
    usuarioModel.setSexo(Sexo);
    usuarioModel.setEmail(Email);
    usuarioModel.setSenha(Senha);
    usuarioModel.setConfirma_Senha(Confirma_Senha);

    UsuarioDAO usuarioDAO = new UsuarioDAO();
    usuarioDAO.CadastrarUsuario(usuarioModel);
}
   
